# 通用环境变量

# 编辑器
export EDITOR="nvim"
export VISUAL="nvim"

# NVM
export NVM_DIR="$HOME/.nvm"
[ -s "$NVM_DIR/nvm.sh" ] && \. "$NVM_DIR/nvm.sh"
[ -s "$NVM_DIR/bash_completion" ] && \. "$NVM_DIR/bash_completion"

# pyenv
export PATH="$HOME/.pyenv/bin:$PATH"

# 本地 bin
export PATH="$HOME/.local/bin:$PATH"
